// operadores relacionales o de comparacion

// mayor que >, menor  que >, mayor o igual >=, menor o igual que <=, igual que === y distinto que !==

let mayorQue = 20 > 10;
console.log(mayorQue)

let menorQue = 30 < 20;
console.log(menorQue)

let igualQue = 20 === 10;
console.log(igualQue)

let diferenteQue = 20 !== 10;
console.log(diferenteQue);

let diferente = 10 != "10";
console.log(diferente); 

// operadores logicos

// &&: si los dos son verdaderos devuelve "verdadero"

let verdaderoUno = true && true;
console.log(verdaderoUno);

let verdaderoDos = 20 > 10 && 10 < 20;
console.log(verdaderoDos);

let verdaderoTres = true && true && false;
console.log(verdaderoTres); 

// ||: con que uno sea verdadero devuelve "verdadero

let verdaderoCuatro = true || false;
console.log(verdaderoCuatro);



// !: Negacion

let verdaderoCinco = !false;
console.log(verdaderoCinco);